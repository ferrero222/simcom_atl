/*******************************************************************************
 * Global type definitions ('typedef')
 ******************************************************************************/
/**
  ** @brief Union for crc calc
**/ 
union sim_crc_type
{
  uint32_t crc32;
  struct
  {
    uint16_t crc16_1;
    uint16_t crc16_2;
  } words;
  struct
  {
    uint8_t crc16l;
    uint8_t crc16h;
    uint8_t crc8;
    uint8_t crc8_2;
  } bytes;
};
typedef union sim_crc_type sim_proc_crc_type;

/*******************************************************************************
 * Global variable definitions ('extern')
 ******************************************************************************/
extern sim_proc_crc_type sim_proc_crc;

